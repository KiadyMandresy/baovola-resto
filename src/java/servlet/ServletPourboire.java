    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, Exception {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            if(request.getParameter("date1")!=null && request.getParameter("date2")!=null){
                Pourboire p =new Pourboire();
                Vector list = p.pourboire(request.getParameter("date1"), request.getParameter("date2"));
                
                request.setAttribute("list", list);
                request.setAttribute("date1", request.getParameter("date1"));
                request.setAttribute("date2", request.getParameter("date2"));
            }
            
            String view = "listepourboire.jsp";
            request.setAttribute("view",view);
            RequestDispatcher dispat = request.getRequestDispatcher("/templateAdmin.jsp");
            dispat.forward(request,response);
        }
    }

